#include<iostream>
using namespace std;
int main()
{
    // variable declaration
    int a;
    // variable initialization
    a = 10;
    cout<<"The value of a is "<<a;
    //int is o 4 bytes in size of 32 bits
    // long int is of 8 bytes in size of 64 bits
    // float is of 4 bytes in size of 32 bits with decimal values
    // double is of 8 bytes in size of 64 bits with decimal values
    // char is of 1 bit yes or no value or single character value or simply true or false value
    float b = 3.14;
    cout<<"\nThe value of b is "<<b;
    double c = 3.1415926535897932384626433832795;
    cout<<"\nThe value of c is "<<c;
    char d = 'A';
    cout<<"\nThe value of d is "<<d;
}